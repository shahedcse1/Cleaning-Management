<script>

    function isNumberKey(evt) {
        var charCode = (evt.which) ? evt.which : evt.keyCode;
        if (charCode > 31 && (charCode < 48 || charCode > 57))
            return false;
        return true;
    }

    function send_message() {
        var name = $("#name").val();
        var email = $("#email").val();
        var phone = $("#phone").val();
        var subject = $("#subject").val();
        var message = $("#message").val();

        if (name == '' || email == '' || phone == '' || message == '') {
            $("#failed").show();
            $("#success").hide();
        }
        else {
            $.ajax({
                type: "GET",
                url: "<?= base_url('Contact/send_message'); ?>",
                data: {
                    name: name,
                    email: email,
                    phone: phone,
                    subject: subject,
                    message: message
                }
            });
            $("#name").val("");
            $("#email").val("");
            $("#phone").val("");
            $("#subject").val("");
            $("#message").val("");
            $("#failed").hide();
            $("#success").show();
        }
    }
</script>
