                                            <li>   Store dry foods in tightly sealed containers</li>
                                            <li>   Rinse cans, bottles and plastics before putting them in recycling bins</li>
                                            <li>  Empty the rubbish on a daily basis</li><br>
                                            Clean - Clear all waste food and liquid spillage, cleaning up food debris from food preparation areas, under sinks and appliances. Remove pet food, drink and litter trays before nightfall.
                                            De-clutter - Cockroaches release an aggregation pheromone in their droppings telling others they have found a safe harborage. Remove old stacks of newspapers and magazines, unused cardboard boxes and all other forms of clutter from the floor or bottom of cupboards where these pests have easy access.</p>
